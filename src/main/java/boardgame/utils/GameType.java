package boardgame.utils;

/**
 * Enumeration representing the different types of games supported by the application.
 * Currently supports Snakes and Ladders and Ludo.
 */
public enum GameType {
    /**
     * Represents the Snakes and Ladders game.
     */
    SnakesNLadders,
    /**
     * Represents the Ludo game.
     */
    Ludo
}